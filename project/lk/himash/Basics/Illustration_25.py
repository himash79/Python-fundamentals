# Turtle class :
# It's able to create graphical design which is kind of arts.
# Below example able execute in python_roadmap IDLE.

import turtle

t = turtle.Turtle()
t.shape('turtle')
t.forward(100)
t.color('gray')
t.pencolor('yellow')
t.left(90)
t.forward(100)
t.color('orange')
t.left(90)
t.forward(100)
t.color('red')
t.left(90)
t.forward(100)