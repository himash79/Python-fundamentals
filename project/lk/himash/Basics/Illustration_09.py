no01 = 10
no02 = 12
no03 = 5
no04 = 20
print('Equal and greater than / less than operations')
print(no01 == no02)
print(no01 != no02)
print(no01 >= no02)
print(no01 <= no02)
print(no01 > no02)
print(no01 < no02)

